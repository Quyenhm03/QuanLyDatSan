package model;

public class MatHang39 {
    private int id;
    private String maMH, tenMH;
    private float donGia;

    public MatHang39() {
    }

    public MatHang39(int id, String maMH, String tenMH, float donGia) {
        this.id = id;
        this.maMH = maMH;
        this.tenMH = tenMH;
        this.donGia = donGia;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMaMH() {
        return maMH;
    }

    public void setMaMH(String maMH) {
        this.maMH = maMH;
    }

    public String getTenMH() {
        return tenMH;
    }

    public void setTenMH(String tenMH) {
        this.tenMH = tenMH;
    }

    public float getDonGia() {
        return donGia;
    }

    public void setDonGia(float donGia) {
        this.donGia = donGia;
    }
}
